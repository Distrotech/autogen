# ----------------------------------------------------------------------

exec 5> temp.config

GENLIST="agen5/autogen.1
	 agen5/directive.h
	 agen5/expr.h
	 agen5/expr.ini
	 agen5/functions.h
	 agen5/opts.c
	 agen5/opts.h
	 agen5/autogen.menu
	 agen5/autogen.texi
	 autoopts/genshell.c
	 autoopts/genshell.h"

rm -f ./configure
cvs get configure

sed -n '1,/^SRCDIR/p' configure >&5

cat <<_EOF_ >&5

if [ ! -f ./configure.in ]
then
  echo cannot locate top source directory for AutoGen
  exit 1
fi

`for f in ./configure ${GENLIST}
do echo $f ; done | \
columns -I10 --first='for f in ' --line=' \' `
do
  if [ -f \$f ]
  then
    rm -f \$f || {
      echo cannot remove \$f
      exit 1
    }
  fi
done

_EOF_

shar -T -w -D -Q -m ${GENLIST} | \
  sed -e '1,/== agen5\/autogen.1 ==/d' \
      -e '/DO NOT EDIT THIS FILE/,/and the template file/d' \
      -e '/lock directory/,$d' \
  >&5

cat <<'_EOF_' >&5

sh config/bootstrap $@ || exit 1
cd $SVDIR
sh ${SRCDIR}/configure $@
_EOF_
echo "# configure ends here" >&5

exec 5>&-

if cmp configure temp.config
then
  echo configure is unchanged
  rm -f temp.config
else
  cvs edit configure
  mv -f temp.config configure
  cvs commit -m'CVS-ed configure script update' configure
fi

# :mkconfig ends here
